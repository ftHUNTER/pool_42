#include<unistd.h>

int	main(int argc, char *argv[])
{
	int	j;
	int	i;

	i = 1;
	j = 0;
	if (argc >= 2)
	{
		while (i < argc)
		{
			while (argv[i][j] != '\0')
			{
				write(1, &argv[i][j], 1);
				j++;
			}
			j = 0;
			i++;
			write(1, "\n", 1);
		}
	}
	return (0);
}
