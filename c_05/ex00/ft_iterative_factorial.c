int	ft_iterative_factorial(int nb)
{
	int	x;

	x = nb - 1;
	if (nb >= 0)
	{
		if (nb == 0 || nb == 1)
			return (1);
		while (x > 0)
		{
			nb = nb * x;
			x--;
		}
		return (nb);
	}
	return (0);
}
